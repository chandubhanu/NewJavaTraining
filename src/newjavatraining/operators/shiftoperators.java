package newjavatraining.operators;

public class shiftoperators {

	public static void main(String[] args) {
		
		
		// Right Shift >>(divison)
		System.out.println(10>>2);
		System.out.println(-10>>2);
		//Left Shift(Multiply)
		System.out.println(10<<2);
		System.out.println(-10<<2);
		//Shift Operator
		System.out.println(10>>>2);
		System.out.println(-10>>>2);
		
	}

}
