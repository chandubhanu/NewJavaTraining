package newjavatraining.operators;

public class RelationalOperators {

	public static void main(String[] args) {
		
		//Comparison Operator
		int a=10;
		if(a>6)
		{
			System.out.println(a+"is greater than 6");
		}
		if(a<11)
		{
			System.out.println(a+"is less than 11");
		}
		if(a<=11)
		{
			System.out.println(a+"is less than or equal to 11");
		}
		if(a>=10)
		{
			System.out.println(a+"is greater than or equal to 10");
		}
		//
		if(a==10)
		{
			System.out.println(a+"is equal to 10");
		}
		if(a!=11)
		{
			System.out.println(a+"is not equal to 10");
		}
	}

}
